package com.example.golfapp.model;

public class Player {
	private String name;
	private int tee;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTee() {
		return tee;
	}
	public void setTee(int tee) {
		this.tee = tee;
	}

}
